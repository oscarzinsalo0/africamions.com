export function filterProducts(
  brand?: string,
  category?: string,
  type?: string
): Product[] {
  return products.filter(product => {
    if (brand && product.brand !== brand) return false;
    if (category && product.category !== category) return false;
    if (type && product.type !== type) return false;
    return true;
  });
}
